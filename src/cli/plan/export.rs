//! Plan export to various formats (bash, ansible, terraform)

use super::types::*;
use anyhow::Result;
use std::fmt::Write as FmtWrite;

/// Escape a string for safe interpolation into a shell script.
/// Wraps the value in single quotes with proper inner escaping.
fn shell_escape(s: &str) -> String {
    // Replace each single quote with: end quote, escaped quote, start quote
    let escaped = s.replace('\'', "'\\''");
    format!("'{}'", escaped)
}

/// Escape a string for use inside a sed 's/pattern/replacement/' expression.
/// Escapes sed special characters WITHOUT wrapping in quotes.
fn sed_escape(s: &str) -> String {
    s.replace('\\', "\\\\")
        .replace('/', "\\/")
        .replace('&', "\\&")
        .replace('\n', "\\n")
}

/// Exports fix plans to different formats
pub struct PlanExporter;

impl PlanExporter {
    /// Export plan as bash script
    pub fn to_bash(plan: &FixPlan) -> Result<String> {
        let mut script = String::new();

        writeln!(script, "#!/bin/bash")?;
        writeln!(script, "# Generated by GuestKit v{}", env!("CARGO_PKG_VERSION"))?;
        writeln!(script, "# Profile: {}", plan.profile)?;
        writeln!(script, "# VM: {}", plan.vm)?;
        writeln!(script, "# Generated: {}", plan.generated)?;
        writeln!(script)?;
        writeln!(script, "set -e")?;
        writeln!(script)?;
        writeln!(script, "echo \"Applying {} fixes...\"", plan.profile)?;
        writeln!(script)?;

        // Backup section
        writeln!(script, "# Create backup")?;
        writeln!(script, "BACKUP_DIR=\"/backup/$(date +%Y%m%d-%H%M%S)\"")?;
        writeln!(script, "mkdir -p \"$BACKUP_DIR\"")?;
        writeln!(script)?;

        // Generate operations
        for op in &plan.operations {
            Self::operation_to_bash(&mut script, op)?;
        }

        // Post-apply actions
        if !plan.post_apply.is_empty() {
            writeln!(script, "# Post-apply actions")?;
            for action in &plan.post_apply {
                Self::post_action_to_bash(&mut script, action)?;
            }
        }

        writeln!(script)?;
        writeln!(script, "echo \"✓ All fixes applied successfully\"")?;

        Ok(script)
    }

    /// Convert operation to bash commands
    fn operation_to_bash(script: &mut String, op: &Operation) -> Result<()> {
        writeln!(script, "# {}: {}", op.id, op.description)?;

        match &op.op_type {
            OperationType::FileEdit(fe) => {
                writeln!(script, "cp {} \"$BACKUP_DIR/\"", shell_escape(&fe.file))?;
                for change in &fe.changes {
                    writeln!(
                        script,
                        "sed -i 's/{}/{}/g' {}",
                        sed_escape(&change.before),
                        sed_escape(&change.after),
                        shell_escape(&fe.file)
                    )?;
                }
            }
            OperationType::PackageInstall(pi) => {
                let escaped_pkgs: Vec<String> = pi.packages.iter().map(|p| shell_escape(p)).collect();
                writeln!(script, "dnf install -y {}", escaped_pkgs.join(" "))?;
            }
            OperationType::ServiceOperation(so) => {
                if let Some(state) = &so.state {
                    if state == "enabled" {
                        writeln!(script, "systemctl enable {}", shell_escape(&so.service))?;
                    }
                }
                if so.start {
                    writeln!(script, "systemctl start {}", shell_escape(&so.service))?;
                }
                if so.restart {
                    writeln!(script, "systemctl restart {}", shell_escape(&so.service))?;
                }
            }
            OperationType::SelinuxMode(sm) => {
                writeln!(
                    script,
                    "sed -i 's/SELINUX={}/SELINUX={}/g' {}",
                    sed_escape(&sm.current), sed_escape(&sm.target), shell_escape(&sm.file)
                )?;
            }
            OperationType::CommandExec(ce) => {
                // WARNING: CommandExec writes commands directly to the script.
                // Ensure ce.command comes from a trusted/validated source.
                writeln!(script, "# [CommandExec] Review the following command before running:")?;
                writeln!(script, "{}", ce.command)?;
            }
            OperationType::FileCopy(fc) => {
                writeln!(script, "cp {} {}", shell_escape(&fc.source), shell_escape(&fc.destination))?;
            }
            OperationType::DirectoryCreate(dc) => {
                if let Some(mode) = &dc.mode {
                    writeln!(script, "mkdir -p -m {} {}", shell_escape(mode), shell_escape(&dc.path))?;
                } else {
                    writeln!(script, "mkdir -p {}", shell_escape(&dc.path))?;
                }
            }
            OperationType::FilePermissions(fp) => {
                writeln!(script, "chmod {} {}", shell_escape(&fp.mode), shell_escape(&fp.path))?;
                if let Some(owner) = &fp.owner {
                    writeln!(script, "chown {} {}", shell_escape(owner), shell_escape(&fp.path))?;
                }
            }
            OperationType::RegistryEdit(re) => {
                let new_data_str = re.new_data.to_string();
                writeln!(script, "echo \"Registry edit: {} \\\\ {} = {} (type: {})\"",
                    shell_escape(&re.key), shell_escape(&re.value), shell_escape(&new_data_str), shell_escape(&re.data_type))?;
                writeln!(script, "echo \"WARNING: Registry edits require Windows tools (reg.exe or regedit)\"")?;
            }
        }

        if let Some(validation) = &op.validation {
            // Intentionally not escaped: validation.command is a trusted shell command
            writeln!(script, "{} || {{ echo \"Validation failed for {}\"; exit 1; }}",
                validation.command, shell_escape(&op.id))?;
        }

        writeln!(script)?;
        Ok(())
    }

    /// Convert post-apply action to bash
    fn post_action_to_bash(script: &mut String, action: &PostApplyAction) -> Result<()> {
        match action {
            PostApplyAction::ServiceRestart { services } => {
                for service in services {
                    writeln!(script, "systemctl restart {}", shell_escape(service))?;
                }
            }
            PostApplyAction::Validation { command, .. } => {
                // Intentionally not escaped: validation command is a trusted shell command
                writeln!(script, "{}", command)?;
            }
            PostApplyAction::Message { message } => {
                writeln!(script, "echo {}", shell_escape(message))?;
            }
            PostApplyAction::RebootRequired { reason } => {
                writeln!(script, "echo {}", shell_escape(&format!("Reboot required: {}", reason)))?;
            }
        }
        Ok(())
    }

    /// Export plan as Ansible playbook
    pub fn to_ansible(plan: &FixPlan) -> Result<String> {
        let mut playbook = String::new();

        writeln!(playbook, "---")?;
        writeln!(playbook, "- name: GuestKit {} Fixes", plan.profile)?;
        writeln!(playbook, "  hosts: vm")?;
        writeln!(playbook, "  become: yes")?;
        writeln!(playbook, "  tasks:")?;

        for op in &plan.operations {
            Self::operation_to_ansible(&mut playbook, op)?;
        }

        Ok(playbook)
    }

    /// Convert operation to Ansible task
    fn operation_to_ansible(playbook: &mut String, op: &Operation) -> Result<()> {
        writeln!(playbook, "    - name: {}", op.description)?;

        match &op.op_type {
            OperationType::FileEdit(fe) => {
                if let Some(change) = fe.changes.first() {
                    writeln!(playbook, "      lineinfile:")?;
                    writeln!(playbook, "        path: {}", fe.file)?;
                    writeln!(playbook, "        regexp: '^{}$'", change.before)?;
                    writeln!(playbook, "        line: '{}'", change.after)?;
                    writeln!(playbook, "        backup: yes")?;
                }
            }
            OperationType::PackageInstall(pi) => {
                writeln!(playbook, "      package:")?;
                writeln!(playbook, "        name:")?;
                for pkg in &pi.packages {
                    writeln!(playbook, "          - {}", pkg)?;
                }
                writeln!(playbook, "        state: present")?;
            }
            OperationType::ServiceOperation(so) => {
                writeln!(playbook, "      service:")?;
                writeln!(playbook, "        name: {}", so.service)?;
                if let Some(state) = &so.state {
                    writeln!(playbook, "        enabled: {}", state == "enabled")?;
                }
                if so.start {
                    writeln!(playbook, "        state: started")?;
                }
            }
            OperationType::SelinuxMode(sm) => {
                writeln!(playbook, "      lineinfile:")?;
                writeln!(playbook, "        path: {}", sm.file)?;
                writeln!(playbook, "        regexp: '^SELINUX={}'", sm.current)?;
                writeln!(playbook, "        line: 'SELINUX={}'", sm.target)?;
                writeln!(playbook, "        backup: yes")?;
            }
            OperationType::CommandExec(ce) => {
                writeln!(playbook, "      command: {}", ce.command)?;
            }
            OperationType::FileCopy(fc) => {
                writeln!(playbook, "      copy:")?;
                writeln!(playbook, "        src: {}", fc.source)?;
                writeln!(playbook, "        dest: {}", fc.destination)?;
                writeln!(playbook, "        remote_src: yes")?;
                if fc.backup {
                    writeln!(playbook, "        backup: yes")?;
                }
            }
            OperationType::DirectoryCreate(dc) => {
                writeln!(playbook, "      file:")?;
                writeln!(playbook, "        path: {}", dc.path)?;
                writeln!(playbook, "        state: directory")?;
                if let Some(mode) = &dc.mode {
                    writeln!(playbook, "        mode: '{}'", mode)?;
                }
            }
            OperationType::FilePermissions(fp) => {
                writeln!(playbook, "      file:")?;
                writeln!(playbook, "        path: {}", fp.path)?;
                writeln!(playbook, "        mode: '{}'", fp.mode)?;
                if let Some(owner) = &fp.owner {
                    writeln!(playbook, "        owner: {}", owner)?;
                }
                if let Some(group) = &fp.group {
                    writeln!(playbook, "        group: {}", group)?;
                }
            }
            OperationType::RegistryEdit(re) => {
                writeln!(playbook, "      win_regedit:")?;
                writeln!(playbook, "        path: {}", re.key)?;
                writeln!(playbook, "        name: {}", re.value)?;
                writeln!(playbook, "        data: {}", re.new_data)?;
                writeln!(playbook, "        type: {}", re.data_type)?;
            }
        }

        writeln!(playbook)?;
        Ok(())
    }

    /// Export plan as JSON
    pub fn to_json(plan: &FixPlan) -> Result<String> {
        Ok(serde_json::to_string_pretty(plan)?)
    }

    /// Export plan as YAML
    pub fn to_yaml(plan: &FixPlan) -> Result<String> {
        Ok(serde_yaml::to_string(plan)?)
    }
}

